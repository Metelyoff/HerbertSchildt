package chapter7_classes_and_methods;

class LengthArray {
	public static void main(String[] args) {
		int a1[] = new int[10];
		int a2[] = { 1, 2, 3, 4, 5 };
		int a3[] = { 9, 8, 7, 6 };

		System.out.println("a1 = " + a1.length);
		System.out.println("a2 = " + a2.length);
		System.out.println("a3 = " + a3.length);
	}

}
